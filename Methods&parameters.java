//method that takes a String called fname as parameter. When the method is called, 
//we pass along a first name, which is used inside the method to print the full name
public class Main {
  static void myMethod(String fname) {
    System.out.println(fname + " " + "Okotkotber");
  }
    public static void main(String[] args) {
      myMethod("Mary");
      myMethod("Danny");
      myMethod("Grace");
    }
  }

//output
//Mary Okotkotber
//Danny Okotkotber
//Grace Okotkotber
      

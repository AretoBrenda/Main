//You can have as many parameters as you like:

public class Main {
  static void myMethod(String fname, int age) {
    System.out.println(fname + " is " + age);
  }
  public static void main(String[] args) {
    myMethod("Mary", 10);
    myMethod("Jonnie", 35);
    myMethod("Brenda", 39);
  }
}
//prints out Name plus age
